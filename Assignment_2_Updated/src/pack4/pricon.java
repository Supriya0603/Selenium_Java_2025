package pack4;

public class pricon 
{

	int result;

	public pricon(int a, int b)
	
	{
		int ans = b - a;
		result = ans;
		
		
	}
	
	public static void main(String[] args)

	{

		pricon c5 = new pricon(10, 20);
		System.out.println("subtraction of two numbers is " + c5.result);

	}
	
	

}
